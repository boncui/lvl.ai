import mongoose, { Document, Schema } from 'mongoose';

// ---------- ENUMS ----------
export enum TaskType {
  FOOD = 'food',
  HOMEWORK = 'homework',
  EMAIL = 'email',
  MEETING = 'meeting',
  PROJECT = 'project',
  PERSONAL = 'personal',
  WORK = 'work',
  HEALTH = 'health',
  SOCIAL = 'social',
  OTHER = 'other',
}

export enum TaskPriority {
  LOW = 'low',
  MEDIUM = 'medium',
  HIGH = 'high',
  URGENT = 'urgent',
}

export enum TaskStatus {
  PENDING = 'pending',
  IN_PROGRESS = 'in_progress',
  COMPLETED = 'completed',
  CANCELLED = 'cancelled',
}

// ---------- BASE TASK INTERFACE ----------
export interface IBaseTask extends Document {
  // Core task fields
  title: string;
  description?: string;
  taskType: TaskType;
  priority: TaskPriority;
  status: TaskStatus;
  dueDate?: Date;
  startDate?: Date;
  completedAt?: Date;
  estimatedDuration?: number; // in minutes
  actualDuration?: number; // in minutes
  tags: string[];
  
  // User relationships
  assignee: mongoose.Types.ObjectId; // User who owns the task
  assignedBy?: mongoose.Types.ObjectId; // User who assigned the task (if different from assignee)
  collaborators: mongoose.Types.ObjectId[]; // Users who can help with the task
  
  // Task organization
  location?: string;
  isRecurring: boolean;
  recurringPattern?: {
    frequency: 'daily' | 'weekly' | 'monthly' | 'yearly';
    interval: number; // every X days/weeks/months/years
    daysOfWeek?: number[]; // 0-6 for Sunday-Saturday (for weekly)
    dayOfMonth?: number; // 1-31 (for monthly)
    endDate?: Date;
  };
  parentTask?: mongoose.Types.ObjectId; // For subtasks
  subtasks: mongoose.Types.ObjectId[]; // Array of subtask IDs
  
  // Task content
  attachments: {
    filename: string;
    url: string;
    uploadedAt: Date;
  }[];
  notes: {
    content: string;
    createdAt: Date;
    createdBy: mongoose.Types.ObjectId;
  }[];
  reminders: {
    date: Date;
    message: string;
    isSent: boolean;
  }[];
  
  // XP and gamification
  xpValue: number;
  autoGenerated?: boolean; // if AI created it
  
  // Personal task fields (optional for all tasks)
  personalCategory?: 'health' | 'fitness' | 'hobby' | 'learning' | 'travel' | 'family' | 'finance' | 'home' | 'other';
  isPrivate?: boolean;
  personalNotes?: string;
  moodBefore?: string;
  moodAfter?: string;
  energyLevel?: 1 | 2 | 3 | 4 | 5; // 1 = very low, 5 = very high
  weatherDependent?: boolean;
  weatherCondition?: string;
  seasonality?: string[]; // e.g., ['spring', 'summer']
  personalGoals?: string[];
  reflection?: string;
  photos?: string[]; // URLs to photos
  cost?: number;
  actualCost?: number;
  
  createdAt: Date;
  updatedAt: Date;
}

