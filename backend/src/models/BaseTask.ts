// Indexes for better query performance
BaseTaskSchema.index({ assignee: 1, status: 1 });
BaseTaskSchema.index({ dueDate: 1 });
BaseTaskSchema.index({ taskType: 1 });
BaseTaskSchema.index({ priority: 1 });
BaseTaskSchema.index({ tags: 1 });

// Personal task indexes
BaseTaskSchema.index({ assignee: 1, personalCategory: 1 });
BaseTaskSchema.index({ assignee: 1, isPrivate: 1 });
BaseTaskSchema.index({ energyLevel: 1 });
BaseTaskSchema.index({ seasonality: 1 });

// Virtual for checking if task is overdue
BaseTaskSchema.virtual('isOverdue').get(function() {
  return this.dueDate && this.dueDate < new Date() && this.status !== TaskStatus.COMPLETED;
});

// Virtual for checking if task affects mood positively
BaseTaskSchema.virtual('improvesMood').get(function() {
  if (!this.moodBefore || !this.moodAfter) return null;
  // Simple mood improvement check - can be enhanced with sentiment analysis
  const positiveMoods = ['happy', 'energized', 'satisfied', 'accomplished', 'relaxed'];
  return positiveMoods.some(mood => this.moodAfter!.toLowerCase().includes(mood));
});

// Virtual for checking if task is weather appropriate
BaseTaskSchema.virtual('isWeatherAppropriate').get(function() {
  if (!this.weatherDependent || !this.weatherCondition) return true;
  // This would integrate with weather API in real implementation
  return true; // Placeholder
});

// Pre-save middleware to set completedAt when status changes to completed
BaseTaskSchema.pre('save', function(next) {
  if (this.isModified('status') && this.status === TaskStatus.COMPLETED && !this.completedAt) {
    this.completedAt = new Date();
  }
  next();
});

// Method to add a note
BaseTaskSchema.methods['addNote'] = function(content: string, userId: string) {
  this['notes'].push({
    content,
    createdBy: userId
  });
  return this['save']();
};

// Method to add a reminder
BaseTaskSchema.methods['addReminder'] = function(date: Date, message?: string) {
  this['reminders'].push({
    date,
    message: message || `Reminder: ${this['title']}`,
    isSent: false
  });
  return this['save']();
};

// Method to add a collaborator
BaseTaskSchema.methods['addCollaborator'] = function(userId: string) {
  if (!this['collaborators'].includes(userId)) {
    this['collaborators'].push(userId);
  }
  return this['save']();
};

// Method to remove a collaborator
BaseTaskSchema.methods['removeCollaborator'] = function(userId: string) {
  this['collaborators'] = this['collaborators'].filter((id: any) => !id.equals(userId));
  return this['save']();
};

// Personal task methods
BaseTaskSchema.methods['addPersonalGoal'] = function(goal: string) {
  if (!this['personalGoals']) {
    this['personalGoals'] = [];
  }
  if (!this['personalGoals'].includes(goal)) {
    this['personalGoals'].push(goal);
  }
  return this['save']();
};

BaseTaskSchema.methods['addPhoto'] = function(photoUrl: string) {
  if (!this['photos']) {
    this['photos'] = [];
  }
  if (!this['photos'].includes(photoUrl)) {
    this['photos'].push(photoUrl);
  }
  return this['save']();
};

BaseTaskSchema.methods['updateMood'] = function(mood: string, isAfter: boolean = false) {
  if (isAfter) {
    this['moodAfter'] = mood;
  } else {
    this['moodBefore'] = mood;
  }
  return this['save']();
};

export { BaseTaskSchema };
export default BaseTaskSchema;
